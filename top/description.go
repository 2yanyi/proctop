import "strings"

func descriptionMatch(s string) (_ string) {
	if utilities[s] {
		return "\u001B[0;37;48mUNIX utilities\u001B[0m"
	}
	if strings.HasPrefix(components[s], "https://github.com") {
		return strings.Join([]string{"\u001B[0;35;48m", components[s], "\u001B[0m"}, "")
	}
	return components[s]
}

